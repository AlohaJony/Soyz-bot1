    if info['type'] == 'single':
        # –û–¥–∏–Ω–æ—á–Ω—ã–π —Ñ–∞–π–ª
        ext = info.get('ext', 'mp4')
        safe_title = re.sub(r'\W+', '', info['title'][:30])
