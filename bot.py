    chat_id = event.message.recipient.chat_id
    status_msg = await event.message.answer("üîç –ü–æ–ª—É—á–∞—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é...")

    info = await asyncio.to_thread(extract_info, url)
